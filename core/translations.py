# Map of strength_label (stored in English in DB) -> translation key
STRENGTH_LABEL_KEYS = {
    'Strong': 'label_strong',
    'Moderate': 'label_moderate',
    'Weak': 'label_weak',
    'Very Weak': 'label_very_weak',
    'Unknown': 'label_unknown',
}


def translate_strength_label(label: str, lang: str) -> str:
    """Translate a stored strength label to the given language."""
    key = STRENGTH_LABEL_KEYS.get(label, 'label_unknown')
    return get_translations(lang).get(key, label)
